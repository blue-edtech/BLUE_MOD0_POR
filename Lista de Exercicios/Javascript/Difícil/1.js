// solicita a largura do terreno
let largura = prompt("Digite a largura do terreno em metros: ");

// solicita o comprimento do terreno
let comprimento = prompt("Digite o comprimento do terreno em metros: ");

// calcula a área do terreno
let area = largura * comprimento;

// exibe a área do terreno
console.log(`A área do terreno é ${area} metros quadrados.`);
